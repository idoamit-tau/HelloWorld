public class Exmple3 {
    public static void main(String[] args) {
        int num1 = 15;
        int num2 = num1++;
        System.out.println("num1 is " + num1);
        System.out.println("num2 is " + num2);
        int num3 = 12;
        int num4 = ++num3;
        System.out.println("num3 is " + num3);
        System.out.println("num4 is " + num4);
    }
}
